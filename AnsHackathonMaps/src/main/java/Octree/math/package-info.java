/**
 * Provides a variety of math related classes.
 * <p>
 * The {@code Math} class provides a variety of general math functions,
 * such as greatest-common-divisor, odd/even, mean, median, standard-deviation,
 * variance, polynomials and polynomial-fitting. These methods can be statically
 * imported.
 * <p>
 * The {@code Complex} class provides support for complex numbers.
 * <p>
 * The class {@code Matrix} provides real matrix arithmetic. This includes
 * all common matrix operators, and various common decompositions
 * (QR, LU, Cholesky, Singular Value, Eigen).
 * <p>
 * The classes {@code Vector} and {@code ComplexVector} are
 * real and complex vector implementations. All common operators are
 * available. Additionaly, the {@code ComplexVector} provides
 * Fast Fourier Transformation and convolution support.
 */
package Octree.math;
